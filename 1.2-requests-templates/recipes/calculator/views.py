def cooking(request, recipe):
    quantity = int(request.GET.get("servings", 1))
    food = dict(DATA[recipe])
    for items in food:
        food[items] = food.get(items)*quantity

    context = dict(
       recipe=food
    )

    return render(request, 'calculator/index.html', context)
